    private void fridgeTableMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_fridgeTableMouseReleased
        // https://stackoverflow.com/questions/10128064/jtable-selected-row-click-event
        JTable source = (JTable)evt.getSource();
        int row = source.rowAtPoint( evt.getPoint() );
        String foodName = String.valueOf(source.getModel().getValueAt(row, 0));
        
        Food food = fridge.get(foodName);
        int quantity = fridge.getQuantity(food);
        
        // https://stackoverflow.com/questions/1481405/how-to-make-a-jframe-modal-in-swing-java
        JDialog frame = new JDialog(this, "Modify Food", true);
        frame.getContentPane().add(new ModifyFoodJFrame(frame, this, food, quantity).mainPanel);
        frame.pack();
        frame.setVisible(true);
    }//GEN-LAST:event_fridgeTableMouseReleased

