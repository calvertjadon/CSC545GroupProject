    public void populateTable() {
        int numRows = fridge.contents.size();
        DefaultTableModel dtm = (DefaultTableModel) fridgeTable.getModel();
        dtm.setNumRows(0);
        dtm.setNumRows(numRows);
        
        Food[] foods = (Food[]) fridge.contents.keySet().toArray(new Food[0]);
        Integer[] quantities = (Integer[]) fridge.contents.values().toArray(new Integer[0]);
        
        for (int i = 0; i < numRows; i++) {
            fridgeTable.setValueAt(foods[i].getName(), i, 0);
            fridgeTable.setValueAt(quantities[i], i, 1);
        }
    }

